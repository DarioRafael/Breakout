_cache: dict[tuple, pygame.Surface] = {}


def _glyph_surface(ch: str, scale: int, color) -> pygame.Surface:
    rows = _G.get(ch) or _G.get(ch.upper()) or _G[" "]
    surf = pygame.Surface((GLYPH_W * scale, GLYPH_H * scale), pygame.SRCALPHA)
    for ry, row in enumerate(rows):
        for cx, c in enumerate(row):
            if c == "#":
                surf.fill(color, (cx * scale, ry * scale, scale, scale))
    return surf


def text_width(text: str, scale: int) -> int:
    if not text:
        return 0
    return (len(text) * ADVANCE - SPACING) * scale


def text_height(scale: int) -> int:
    return GLYPH_H * scale


def render(text: str, scale: int, color) -> pygame.Surface:
    """Return a cached surface with *text* rendered in the bitmap font."""
    scale = max(1, int(scale))
    color = (int(color[0]), int(color[1]), int(color[2]))
    text = str(text).upper()
    key = (text, scale, color)
    surf = _cache.get(key)
    if surf is not None:
        return surf

    w = max(1, text_width(text, scale))
    h = GLYPH_H * scale
    surf = pygame.Surface((w, h), pygame.SRCALPHA)
    x = 0
    for ch in text:
        if ch != " ":
            surf.blit(_glyph_surface(ch, scale, color), (x, 0))
        x += ADVANCE * scale
    _cache[key] = surf
    return surf
